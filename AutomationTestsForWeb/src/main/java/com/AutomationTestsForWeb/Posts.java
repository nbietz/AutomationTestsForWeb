package com.AutomationTestsForWeb;

public class Posts {

  public String id;
  public String title;
  public String author;

  public void setId (String id) {

    this.id = id;
  }

  public void setTitle (String title) {

    this.title = title;
  }

  public void setAuthor (String author) {

    this.author = author;

  }

  public String getId () {

    return id;

  }

  public String getTitle () {

    return title;
  }

  public String getAuthor () {

    return author;
  }

}